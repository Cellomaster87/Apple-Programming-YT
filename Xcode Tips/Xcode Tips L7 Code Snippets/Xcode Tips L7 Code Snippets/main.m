/*!
 Cmd-Shift-L to open the Library
 To create a token type < (less than) # (pound) <nameOfObject> # (pound) > (greater than)
 */

#import <Foundation/Foundation.h>

int main(int argc, const char * argv[]) {
    @autoreleasepool {
        // insert code here...
        NSLog(@"Hello, World!");
    }
    
    do {
        <#statements#>
    } while (<#condition#>);
    
    NSLog(@"%@", <#SomeObject#>);
    
    return 0;
}
